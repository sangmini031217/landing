let currentLang = 'ko';

function setLanguage(lang) {
  currentLang = lang;
  const t = translations[lang];
  if (!t) return;

  // Set direction for Urdu (RTL)
  document.documentElement.setAttribute('dir', lang === 'ur' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang === 'ur' ? 'ur' : lang === 'zh' ? 'zh' : lang === 'en' ? 'en' : 'ko');

  // Apply all translations
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });

  // Update active button state
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('lang-btn--active', btn.getAttribute('data-lang') === lang);
  });

  // Save preference
  try { localStorage.setItem('preferred-lang', lang); } catch (e) {}
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  // Check saved preference
  try {
    const saved = localStorage.getItem('preferred-lang');
    if (saved && translations[saved]) {
      setLanguage(saved);
    }
  } catch (e) {}

  // Bind buttons
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });
});
